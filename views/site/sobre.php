            <h4>História do Grêmio Foot-Ball Porto Alegrense</h4>

            <p>
              No início do século XX, o futebol vinha aos poucos se tornando conhecido no país, 
              semeado por viajantes que levavam a novas fronteiras, suas rudimentares e valiosas 
              bolas de couro. Foi justamente um destes desbravadores, o paulista Cândido Dias da 
              Silva, quem apresentou a primeira bola de futebol a Porto Alegre. A novidade logo 
              despertou o interesse da turma de amigos da Praça XV – vários funcionários dos pequenos 
              comércios da região e alguns alfaiates e farmacêuticos. Ainda em 1902, começou a 
              prática ainda um tanto empírica do esporte, em reuniões dominicais com direito a 
              piqueniques, que fortaleceram ainda mais a convivência daquele grupo.
            </p>
            <p>
              Até que, no feriado de 7 de setembro de 1903, dois quadros de atletas do Sport Clube 
              Rio Grande (cidade portuária e, não por acaso, o clube mais antigo do Brasil) vieram 
              à cidade para uma demonstração – uma ótima oportunidade para os porto-alegrenses 
              aprenderem mais sobre o esporte. O público lotou o campo improvisado para a 
              apresentação e vibrava com as jogadas. Até que, para decepção geral, a bola murchou.
            </p>
            <p>
              Quando todos pensavam que a festa estava terminada, Cândido ofereceu sua bola para 
              que a partida terminasse. Após o jogo, ele e o grupo de amigos puderam confraternizar 
              com os jogadores, que lhe explicaram mais detalhes do esporte e principalmente, o 
              que era necessário para fundar um clube.
              Entusiasmados com o que haviam aprendido, uma semana depois, ao entardecer do dia 15 
              de setembro de 1903, os rapazes reuniram-se em um restaurante em frente à Praça XV 
              escolheram a diretoria e escreveram a ata de fundação, subescrita por 31 nomes. O 
              Grêmio nasceu da bola e da amizade. Naquele momento, iniciou-se a trajetória de um 
              clube vencedor. Carlos Luiz Bohrer foi eleito o primeiro Presidente, sem imaginar a 
              projeção que o "Grêmio de Foot-Ball Porto Alegrense" alcançaria.</p>
